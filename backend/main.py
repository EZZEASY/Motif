async def _state_msg(state: str, character_id: str) -> dict:
    """Build a state_change message, including animation_url if available.

    Uses fallback logic: if no video for this state, use rest state's video
    and trigger background generation for the missing state.
    """
    ctx = get_context(character_id)
    msg = {
        "type": "state_change",
        "state": state,
        "label": ctx.registry.get_label(state),
    }
    url, is_fallback = await ctx.library.get_animation_url_with_fallback(state)
    if url:
        msg["animation_url"] = url
    if is_fallback:
        msg["is_fallback"] = True
    # Trigger background generation if state has no own animation
    if is_fallback or (not url and ctx.registry.is_valid(state)):
        from backend.agent.callbacks import _maybe_trigger_generation
        _maybe_trigger_generation(state, character_id=character_id)
    return msg


# --- Chat history persistence ---

def _chat_history_path(character_id: str) -> str:
    return os.path.join(LOCAL_CHARACTERS_DIR, character_id, "chat_history.json")


def _load_chat_history(character_id: str) -> list[dict]:
    path = _chat_history_path(character_id)
    if not os.path.isfile(path):
        return []
    try:
        with open(path, "r") as f:
            return json.load(f)
    except (json.JSONDecodeError, IOError):
        return []


def _append_chat_messages(character_id: str, messages: list[dict]) -> None:
    history = _load_chat_history(character_id)
    history.extend(messages)
    path = _chat_history_path(character_id)
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        json.dump(history, f, ensure_ascii=False)



@app.get("/api/characters/{character_id}/status")
async def api_get_character_status(character_id: str):
    """Check if a character can be restored (files exist on disk)."""
    char_dir = os.path.join(LOCAL_CHARACTERS_DIR, character_id)
    if not os.path.isdir(char_dir):
        return {"ok": False}

    # Check for images in images/ subdir or root
    images_dir = os.path.join(char_dir, "images")
    has_image = False
    for d in (images_dir, char_dir):
        if os.path.isdir(d):
            has_image = any(
                os.path.isfile(os.path.join(d, name))
                for name in ("upload.png", "0.png", "1.png", "2.png", "3.png")
            )
            if has_image:
                break

    if not has_image:
        return {"ok": False}

    # Check that we have state definitions and animations for this character
    ctx = get_context(character_id)
    all_states = ctx.registry.get_all()
    has_animations = any(
        ctx.library.has_animation_for_state(s) for s in all_states
    )
    if not all_states or not has_animations:
        return {"ok": False}

    return {"ok": True}



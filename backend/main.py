class GenerateCharacterRequest(BaseModel):
    character_id: str
    description: str
    user_id: str = ""


class SelectCharacterRequest(BaseModel):
    character_id: str
    image_index: int


@app.post("/api/generate-character")
async def api_generate_character(req: GenerateCharacterRequest):
    # Store description for later use in state generation
    set_character_description(req.character_id, req.description)

    # Create character directory with images/ subdirectory
    char_dir = os.path.join(LOCAL_CHARACTERS_DIR, req.character_id)
    images_dir = os.path.join(char_dir, "images")
    os.makedirs(images_dir, exist_ok=True)

    paths = await generate_character_images(req.character_id, req.description)
    if not paths:
        return {"images": [], "error": "Generation failed"}

    # Move images to images/ subdirectory
    moved_paths = []
    for p in paths:
        # p is like "{character_id}/0.png"
        basename = os.path.basename(p)
        old_path = os.path.join(LOCAL_CHARACTERS_DIR, p)
        new_path = os.path.join(images_dir, basename)
        if os.path.isfile(old_path) and old_path != new_path:
            os.rename(old_path, new_path)
        moved_paths.append(f"/characters/{req.character_id}/images/{basename}")

    # Write meta.json
    _write_meta(req.character_id, req.user_id, req.description)

    return {"images": moved_paths}


@app.post("/api/select-character")
async def api_select_character(req: SelectCharacterRequest):
    # Build the absolute path to the selected image (now in images/ subdir)
    image_filename = f"{req.image_index}.png"
    abs_path = os.path.join(LOCAL_CHARACTERS_DIR, req.character_id, "images", image_filename)
    if not os.path.isfile(abs_path):
        # Fallback: try old location (without images/ subdir)
        abs_path_old = os.path.join(LOCAL_CHARACTERS_DIR, req.character_id, image_filename)
        if os.path.isfile(abs_path_old):
            abs_path = abs_path_old
        else:
            return {"ok": False, "error": "Image not found"}

    set_character_image(req.character_id, abs_path)

    # Update meta.json with selected image
    _update_meta_selected_image(req.character_id, image_filename)

    # Pre-generate basic state animations in background (states generated by LLM)
    asyncio.create_task(pre_generate_basic_states(req.character_id))

    # Return the URL — determine correct path
    if "images" in abs_path:
        image_url = f"/characters/{req.character_id}/images/{image_filename}"
    else:
        image_url = f"/characters/{req.character_id}/{image_filename}"

    return {"ok": True, "character_image": image_url}


@app.post("/api/upload-character")
async def api_upload_character(
    file: UploadFile = File(...),
    character_id: str = Form(...),
    description: str = Form(""),
    user_id: str = Form(""),
):
    """Accept a user-uploaded character image."""
    if file.content_type not in ("image/png", "image/jpeg", "image/webp"):
        return {"ok": False, "error": "Unsupported file type"}

    char_dir = os.path.join(LOCAL_CHARACTERS_DIR, character_id)
    images_dir = os.path.join(char_dir, "images")
    os.makedirs(images_dir, exist_ok=True)

    dest = os.path.join(images_dir, "upload.png")

    # Read and save (convert to RGBA PNG for consistency)
    from PIL import Image
    import io

    data = await file.read()
    img = Image.open(io.BytesIO(data)).convert("RGBA")
    img.save(dest, "PNG")

    set_character_image(character_id, dest)
    if description:
        set_character_description(character_id, description)

    # Write meta.json
    _write_meta(character_id, user_id, description, selected_image="upload.png")

    asyncio.create_task(pre_generate_basic_states(character_id))
    return {"ok": True, "character_image": f"/characters/{character_id}/images/upload.png"}



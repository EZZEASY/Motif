@app.websocket("/ws/{character_id}")
async def websocket_endpoint(websocket: WebSocket, character_id: str):
    await websocket.accept()
    logger.info(f"WebSocket connected: {character_id}")

    ctx = get_context(character_id)

    # Restore character image from disk if not in memory (page refresh case)
    if not get_character_image(character_id):
        # Try reading from meta.json first
        meta = _read_meta(character_id)
        selected = meta.get("selected_image") if meta else None
        restored = False

        if selected:
            # Try images/ subdir first, then root
            for base in (
                os.path.join(LOCAL_CHARACTERS_DIR, character_id, "images"),
                os.path.join(LOCAL_CHARACTERS_DIR, character_id),
            ):
                path = os.path.join(base, selected)
                if os.path.isfile(path):
                    set_character_image(character_id, path)
                    logger.info(f"Restored character image for {character_id}: {selected}")
                    restored = True
                    break

        if not restored:
            # Fallback: scan for any image
            for search_dir in (
                os.path.join(LOCAL_CHARACTERS_DIR, character_id, "images"),
                os.path.join(LOCAL_CHARACTERS_DIR, character_id),
            ):
                if not os.path.isdir(search_dir):
                    continue
                for name in ("upload.png", "0.png", "1.png", "2.png", "3.png"):
                    path = os.path.join(search_dir, name)
                    if os.path.isfile(path):
                        set_character_image(character_id, path)
                        logger.info(f"Restored character image for {character_id}: {name}")
                        restored = True
                        break
                if restored:
                    break

    # Restore character description from meta.json
    if not get_character_description(character_id):
        meta = _read_meta(character_id)
        if meta and meta.get("description"):
            set_character_description(character_id, meta["description"])

    # Restore existing ADK session or create a new one
    session = None
    try:
        existing = await session_service.list_sessions(
            app_name="motif", user_id=character_id
        )
        if existing and existing.sessions:
            session = await session_service.get_session(
                app_name="motif",
                user_id=character_id,
                session_id=existing.sessions[-1].id,
            )
            logger.info(f"Restored ADK session for {character_id}: {session.id}")
    except Exception as e:
        logger.warning(f"Failed to restore session for {character_id}: {e}")

    if session is None:
        session = await session_service.create_session(
            app_name="motif",
            user_id=character_id,
            state={"_character_id": character_id},
        )
        logger.info(f"Created new ADK session for {character_id}: {session.id}")

    # Send initial state definitions so frontend can generate CSS
    await websocket.send_json({
        "type": "states_init",
        "states": ctx.registry.get_all(),
    })

    # Send initial state (use dynamic default instead of hardcoded "idle")
    default_state = ctx.registry.get_default_state() or "idle"
    await websocket.send_json(await _state_msg(default_state, character_id))

    # Send animation_ready for all existing animations so frontend can populate cache
    # and initialize unlock tracking for states that already have animations
    for state_name in ctx.registry.get_all():
        if ctx.library.has_animation_for_state(state_name):
            record_state(character_id, state_name, character_id=character_id)
            url = await ctx.library.get_animation_url(state_name)
            if url:
                await websocket.send_json({
                    "type": "animation_ready",
                    "state": state_name,
                    "animation_url": url,
                })

    # Send chat history
    chat_history = _load_chat_history(character_id)
    if chat_history:
        await websocket.send_json({
            "type": "chat_history",
            "messages": chat_history,
        })

    # Send initial growth count
    await websocket.send_json({
        "type": "growth",
        "total_animations": get_unlocked_count(character_id),
    })

    state_queue = get_state_queue(character_id)

    # Background task to drain state queue and push to WebSocket
    async def _drain_queue():
        try:
            while True:
                msg = await state_queue.get()
                try:
                    await websocket.send_json(msg)
                except Exception:
                    break
        except asyncio.CancelledError:
            pass

    drain_task = asyncio.create_task(_drain_queue())

    try:
        while True:
            raw = await websocket.receive_text()
            data = json.loads(raw)

            if data.get("type") != "user_message":
                continue

            user_text = data.get("text", "").strip()
            if not user_text:
                continue

            # Set contextvar for tools
            token = current_character_id.set(character_id)

            try:
                # Transition to processing state (content-driven)
                all_states = ctx.registry.get_all()
                processing_state = await animation_router.route_async(
                    user_text, all_states, fallback_role="processing",
                    embedding_cache=ctx.state_embeddings,
                )
                await websocket.send_json(await _state_msg(processing_state, character_id))
                record_state(character_id, processing_state, character_id=character_id)

                # Create content for ADK
                content = types.Content(
                    role="user", parts=[types.Part(text=user_text)]
                )

                # Run agent and stream results
                collected_text = ""

                async for event in runner.run_async(
                    user_id=character_id,
                    session_id=session.id,
                    new_message=content,
                ):
                    # Drain state queue (tool callbacks push state changes here)
                    while not state_queue.empty():
                        state_msg = await state_queue.get()
                        await websocket.send_json(state_msg)

                    # Process agent events
                    if event.content and event.content.parts:
                        for part in event.content.parts:
                            if part.text:
                                # Transition to speaking on first text (content-driven)
                                if not collected_text:
                                    speaking_state = await animation_router.route_async(
                                        part.text, all_states, fallback_role="speaking",
                                        embedding_cache=ctx.state_embeddings,
                                    )
                                    await websocket.send_json(
                                        await _state_msg(speaking_state, character_id)
                                    )
                                    record_state(character_id, speaking_state, character_id=character_id)

                                collected_text += part.text
                                await websocket.send_json(
                                    {
                                        "type": "text_chunk",
                                        "text": part.text,
                                        "done": False,
                                    }
                                )

                # Signal text completion
                if collected_text:
                    await websocket.send_json(
                        {"type": "text_chunk", "text": "", "done": True}
                    )

                # Persist chat history
                import datetime
                ts = datetime.datetime.now(datetime.timezone.utc).isoformat()
                _append_chat_messages(character_id, [
                    {"role": "user", "text": user_text, "timestamp": ts},
                    {"role": "agent", "text": collected_text, "timestamp": ts},
                ])

                # Drain any remaining state changes
                while not state_queue.empty():
                    state_msg = await state_queue.get()
                    await websocket.send_json(state_msg)

                # Send growth message with unlock count
                unlocked_count = get_unlocked_count(character_id)
                await websocket.send_json(
                    {
                        "type": "growth",
                        "total_animations": unlocked_count,
                    }
                )

                # Back to idle — route based on conversation content
                # Re-fetch states (agent may have added new ones via manage_states)
                all_states = ctx.registry.get_all()
                idle_content = user_text + " " + collected_text[:200]
                idle_state = await animation_router.route_async(
                    idle_content, all_states, fallback_role="rest",
                    embedding_cache=ctx.state_embeddings,
                )
                await websocket.send_json(await _state_msg(idle_state, character_id))
            finally:
                current_character_id.reset(token)

    except WebSocketDisconnect:
        logger.info(f"WebSocket disconnected: {character_id}")
    except Exception as e:
        logger.error(f"WebSocket error: {e}", exc_info=True)
        try:
            await websocket.send_json({"type": "error", "message": str(e)})
        except Exception:
            pass
    finally:
        drain_task.cancel()
        cleanup_session(character_id)


# --- Helper functions for meta.json ---

def _write_meta(character_id: str, user_id: str, description: str, selected_image: str = "") -> None:
    """Write meta.json for a character."""
    import datetime
    char_dir = os.path.join(LOCAL_CHARACTERS_DIR, character_id)
    os.makedirs(char_dir, exist_ok=True)
    meta_path = os.path.join(char_dir, "meta.json")

    meta = {}
    if os.path.isfile(meta_path):
        try:
            with open(meta_path, "r") as f:
                meta = json.load(f)
        except (json.JSONDecodeError, IOError):
            pass

    meta["user_id"] = user_id or meta.get("user_id", "")
    meta["description"] = description or meta.get("description", "")
    if selected_image:
        meta["selected_image"] = selected_image
    if "created_at" not in meta:
        meta["created_at"] = datetime.datetime.now(datetime.timezone.utc).isoformat()

    with open(meta_path, "w") as f:
        json.dump(meta, f, indent=2)


def _update_meta_selected_image(character_id: str, image_filename: str) -> None:
    """Update the selected_image field in meta.json."""
    char_dir = os.path.join(LOCAL_CHARACTERS_DIR, character_id)
    meta_path = os.path.join(char_dir, "meta.json")

    meta = {}
    if os.path.isfile(meta_path):
        try:
            with open(meta_path, "r") as f:
                meta = json.load(f)
        except (json.JSONDecodeError, IOError):
            pass

    meta["selected_image"] = image_filename
    with open(meta_path, "w") as f:
        json.dump(meta, f, indent=2)


def _read_meta(character_id: str) -> dict | None:
    """Read meta.json for a character."""
    meta_path = os.path.join(LOCAL_CHARACTERS_DIR, character_id, "meta.json")
    if not os.path.isfile(meta_path):
        return None
    try:
        with open(meta_path, "r") as f:
            return json.load(f)
    except (json.JSONDecodeError, IOError):
        return None


def _get_character_image_url(character_id: str, meta: dict) -> str:
    """Build the image URL for a character from its meta.json."""
    selected = meta.get("selected_image", "")
    if selected:
        # Check images/ subdir first
        images_path = os.path.join(LOCAL_CHARACTERS_DIR, character_id, "images", selected)
        if os.path.isfile(images_path):
            return f"/characters/{character_id}/images/{selected}"
        # Fallback to root
        root_path = os.path.join(LOCAL_CHARACTERS_DIR, character_id, selected)
        if os.path.isfile(root_path):
            return f"/characters/{character_id}/{selected}"

    # Scan for any image
    for subdir in ("images", ""):
        d = os.path.join(LOCAL_CHARACTERS_DIR, character_id, subdir) if subdir else os.path.join(LOCAL_CHARACTERS_DIR, character_id)
        if not os.path.isdir(d):
            continue
        for name in ("upload.png", "0.png", "1.png", "2.png", "3.png"):
            if os.path.isfile(os.path.join(d, name)):
                prefix = f"/characters/{character_id}/{subdir}/{name}" if subdir else f"/characters/{character_id}/{name}"
                return prefix.replace("//", "/")
    return ""



@app.get("/api/characters")
async def api_list_characters(user_id: str = ""):
    """List all characters for a user."""
    characters = []
    if not os.path.isdir(LOCAL_CHARACTERS_DIR):
        return {"characters": characters}

    for char_id in os.listdir(LOCAL_CHARACTERS_DIR):
        meta_path = os.path.join(LOCAL_CHARACTERS_DIR, char_id, "meta.json")
        if not os.path.isfile(meta_path):
            continue
        try:
            with open(meta_path, "r") as f:
                meta = json.load(f)
        except (json.JSONDecodeError, IOError):
            continue

        # Filter by user_id if provided
        if user_id and meta.get("user_id") != user_id:
            continue

        # Build image URL
        image_url = _get_character_image_url(char_id, meta)

        characters.append({
            "character_id": char_id,
            "description": meta.get("description", ""),
            "image_url": image_url,
            "created_at": meta.get("created_at", ""),
        })

    # Sort by created_at descending
    characters.sort(key=lambda c: c.get("created_at", ""), reverse=True)
    return {"characters": characters}


